// Calculating ocean levels
#include <iostream>
using namespace std;

int main()

{
const double mm_per_year = 1.5;
  
  cout << "In 5 years the ocean level will be higher by " << mm_per_year * 5 << " mm" << endl;
  cout << "In 7 years the ocean level will be higher by " << mm_per_year * 7 << " mm" << endl;
  cout << "In 10 years the ocean level will be higher by " << mm_per_year * 10 << " mm" << endl;
  cout << endl;
  
  return 0;
}